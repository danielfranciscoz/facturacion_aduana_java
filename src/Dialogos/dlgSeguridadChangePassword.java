    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        String actual = Cifrado.encrypt(new String(txtActual.getPassword()));
        String pass1 = new String(txtPass1.getPassword());
        String pass2 = new String(txtPass2.getPassword());
        
        if (!actual.isEmpty() && !pass1.isEmpty() && !pass2.isEmpty()) {
            if (actual.equals(SessionHelper.usuario.getPassword())) {
                if (pass1.equals(pass2)) {
                    if (pass1.length() > 5 && pass2.length() > 5) {
                        try {
                            SessionHelper.usuario.setPassword(Cifrado.encrypt(pass1));
                            Dbcontext.actualizar(SessionHelper.usuario);
                            Mensajes.OperacionExitosa(this);
                            dispose();
                        } catch (Exception ex) {
                            Mensajes.ErrorDesconocido(rootPane, ex.getMessage());
                        }
                    } else {
                        txtPass1.setText("");
                        txtPass2.setText("");
                        Mensajes.WarningMessage(this, "Login", "Contraseña Nueva, Tamaño minimo 6 caracteres");
                    }
                } else {
                    txtPass1.setText("");
                    txtPass2.setText("");
                    Mensajes.WarningMessage(this, "Login", "Contraseñas No son iguales");
                }
            } else {
                txtActual.setText("");
                Mensajes.WarningMessage(this, "Login", "Contraseña Actual Erronea!");
            }
        } else {
            Mensajes.InformacionIncompleta(this);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed


