    private void Gestionar(boolean b) {
        if (VerificarCampo()) {
            Mensajes.InformacionIncompleta(this, "\nRazón Social \nRUC \nTelefono \nDirección \nDe igual manera asegurese de escribir de manera correcta el Número RUC");
        } else {
            try {
                int DecisionFinal = JOptionPane.showConfirmDialog(this, (b ? Utilidad.Mensajes.CONFIRMA_INSERSION : Utilidad.Mensajes.CONFIRMA_ACTUALIZACION), "Mensaje de Confirmacion", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (DecisionFinal == 0) {
                    if (GuardarModificar(b)) {
                        Mensajes.OperacionExitosa(this);
                        if (ifrRegistroClientes.isActive) {
                            ifrRegistroClientes.CargarTabla();
                        }

                        if (dlgFacturar.FacturarActiva) {
                            dlgFacturar.CargarClientes(dlgFacturar.OperationType);
                        }
                        LimpiarCampos();
                    }
                }
            } catch (NumberFormatException e) {
            }
        }
    }

    private boolean VerificarCampo() {
        boolean Vacio;
        if (txtRazonSocial.getText().equals("")
                || txtDireccion.getText().equals("")
                || txtRUC.getText().equals("")
                || txtTelefono.getText().equals("    -    ")) {
            Vacio = true;
        } else if (txtRUC.getText().length() < 14) {
            Vacio = true;
        } else {
            Vacio = false;
        }

        return Vacio;
    }

    private boolean GuardarModificar(boolean TipoOperacion) { //True Guardar; False Modificar
        boolean Guardo = false;
        Cliente c;
        if (TipoOperacion) {
            c = new Cliente();
            c.setUsuarioByUserCreacion(SessionHelper.usuario);
            c.setFechaCreacion(new Date());
            c.setUsuarioByUserModificacion(SessionHelper.usuario);
            c.setFechaModificacion(new Date());
            c.setRegAnulado(false);
        } else {
            c = cliente;
            c.setUsuarioByUserModificacion(SessionHelper.usuario);
            c.setFechaModificacion(new Date());
        }

        c.setRazonSocial(txtRazonSocial.getText());
        c.setCorreo(txtCorreo.getText());
        c.setDireccion(txtDireccion.getText());
        c.setRuc(txtRUC.getText());
        c.setTelefono(txtTelefono.getText());

        try {
            if (TipoOperacion) {
                Dbcontext.guardar(c);
            } else {
                Dbcontext.actualizar(c);
                dispose();
            }
            Guardo = true;
        } catch (Exception e) {
            Guardo = false;
            Mensajes.ErrorDesconocido(this, e.getMessage());
        }
        return Guardo;
    }

    private void LimpiarCampos() {
        txtCorreo.setText("");
        txtRUC.setText("");
        txtRazonSocial.setText("");
        txtDireccion.setText("");
        txtTelefono.setValue(null);
    }

    private void CargarInformacion(int IdCliente) {
        clienteBean = new ClienteBean();
        cliente = clienteBean.FindById(IdCliente);
        txtCorreo.setText(cliente.getCorreo());
        txtRUC.setText(cliente.getRuc());
        txtRazonSocial.setText(cliente.getRazonSocial());
        txtDireccion.setText(cliente.getDireccion());
        txtTelefono.setText(cliente.getTelefono());
    }

    private void ActivarValidator() {
        Validador.Validar(txtRUC, Validador.ALFANUMERICO);
    }
