    private String ObtenerPassword(char[] password) throws Exception {
        char[] Pass = password;
        String Password = "";
        for (int i = 0; i < Pass.length; i++) {
            Password = Password + Pass[i];
        }
        return Utilidad.Cifrado.encrypt(Password);
//        return Password;
    }

    private boolean VerificarCampos() {
        return !(txtUsuario.getText().trim().isEmpty() || txtPassword.getPassword().length == 0);
    }

  
