    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        if (VerificarCampos()) {
            /*Verificamos si ya se inicio sesion en hybernate para no cargar tanto la memoria*/
            if (SesionIniciada == false) {
                try {
//                    Dbcontext.IniciarSesion();
                    SesionIniciada = true;
                } catch (Exception ex) {
//                    ex.printStackTrace();
                    Mensajes.ErrorDesconocido(this, ex);
                    SesionIniciada = false;
                    return;
                }
            }

            usuarioBean = new UsuarioBean();
            try {
                usuario = usuarioBean.UsuarioValido(txtUsuario.getText(), ObtenerPassword(txtPassword.getPassword()));
            } catch (Exception ex) {
                Utilidad.Mensajes.ErrorDesconocido(this, ex.getMessage());
                return;
            }

            if (usuario != null) {
                SessionHelper.usuario = usuario;

                empleadoBean = new EmpleadoBean();
                SessionHelper.empleado = empleadoBean.FindById(usuario.getEmpleado().getIdEmpleado());

                personaBean = new PersonaBean();
                SessionHelper.persona = personaBean.FindById(SessionHelper.empleado.getIdEmpleado());

                rolBean = new RolBean();
                SessionHelper.rol = rolBean.FindById(usuario.getRol().getIdRol());

                PermisoBean permisoBean = new PermisoBean();
                SessionHelper.permisos = permisoBean.FindByRol(SessionHelper.rol.getIdRol());

                this.dispose();
                Principal p = new Principal();
                p.setVisible(true);
            } else {
                Mensajes.ErrorMessage(this, "Error de Inicio de Sesión", "Usuario o Contraseña Incorrectos");
            }
        } else {
            Mensajes.InformacionIncompleta(this);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnIniciarActionPerformed

